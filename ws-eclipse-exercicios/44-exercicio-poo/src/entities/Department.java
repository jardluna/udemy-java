package entities;

public class Department {

	//ATTRIBUTES
	private String name;
	
	//CONSTRUCTOR
	public Department() {
		
	}
	
	public Department(String name) {
		this.name = name;
	}
	
	//GETTER AND SETTER
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
